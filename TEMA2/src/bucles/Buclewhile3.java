package bucles;
/*
 * Programa que muestra los numeros del 1 al 11 - sumado se 2 en 2
 */
public class Buclewhile3 {

	public static void main(String[] args) {
		int i = 1;	//Variable contador
		
		while (i <= 11){
			System.out.println(i);
			i= i + 2;
		}
		System.out.println("Fin del bucle");

	}

}
